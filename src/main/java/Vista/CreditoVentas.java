    private void btnResumenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResumenActionPerformed
        // Obtener el total a pagar de la venta
        double totalPagar = 0.0;

        // Recorremos la tabla para calcular el total
        DefaultTableModel model = (DefaultTableModel) jTableCredito.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            // Convertimos el subtotal de la tabla a un número
            String subtotalStr = (String) model.getValueAt(i, 4); // Columna Subtotal
            double subtotal = Double.parseDouble(subtotalStr.replace("$", "").replace(",", "").trim());
            totalPagar += subtotal; // Acumulamos el total
        }

        // Obtener la cantidad de letras seleccionadas
        int letras = Integer.parseInt((String) jComboBoxLetras.getSelectedItem());

        // Calcular el monto mensual
        double montoMensual = totalPagar / letras;

        // Limpiar la tabla de montos
        DefaultTableModel modelResumen = (DefaultTableModel) jTablecredito.getModel();
        modelResumen.setRowCount(0); // Limpiamos la tabla de resumen

        // Agregar las letras y montos a la tabla
        for (int i = 1; i <= letras; i++) {
            modelResumen.addRow(new Object[]{
                i,                                 // N° Letra
                String.format("$%.2f", montoMensual) // Monto
            });
        }

        // Actualizar el JLabel `pagoneto` con el total a pagar
        pagoneto.setText(String.format("Total a Pagar: $%.2f", totalPagar));
    }//GEN-LAST:event_btnResumenActionPerformed

