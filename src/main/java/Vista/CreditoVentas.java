    private void btnAdquirirCreditoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdquirirCreditoActionPerformed
  // Obtener los datos de los campos de texto
        String nombreCliente = getTextCliente();
        String ruc = getTextRuc();
        String nombreProducto = (String) jComboBoxProducto.getSelectedItem();
        int cantidad;

        // Depuración
        System.out.println("Nombre del cliente: '" + nombreCliente + "'");
        System.out.println("RUC: '" + ruc + "'");
        System.out.println("Producto: '" + nombreProducto + "'");

        // Validar que el nombre del cliente no esté vacío
        if (nombreCliente.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingresa un nombre de cliente.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validar RUC
        if (!ruc.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Por favor, ingresa un RUC válido (solo números).", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validar cantidad
        try {
            cantidad = Integer.parseInt(jTextFieldCantidad.getText().trim()); // Aplicar trim aquí
            if (cantidad <= 0) {
                JOptionPane.showMessageDialog(this, "La cantidad debe ser un número positivo.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingresa una cantidad válida (solo números).", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Obtener el precio del producto
        double precio = obtenerPrecioPorProducto(nombreProducto);

        // Calcular subtotal
        double subtotal = cantidad * precio;

        // Agregar los datos a la tabla
        DefaultTableModel model = (DefaultTableModel) jTableCredito.getModel();
        int itemCount = model.getRowCount() + 1;

        model.addRow(new Object[]{
            itemCount,
            nombreProducto,
            cantidad,
            String.format("$%.2f", precio),
            String.format("$%.2f", subtotal)
        });

        // Limpiar los campos de entrada
        jTextFieldCliente.setText("");
        jTextFieldRuc.setText("");
        jTextFieldCantidad.setText("");
}

